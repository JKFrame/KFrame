<div class="contenedor_info_inicio">
  <p>La Comunidad está ubicada en Las Vertientes de La Granja, una tranquila localidad situada a 50 km de la ciudad de Córdoba, a menos de 40 minutos del aeropuerto internacional.
    Se trata de una antigua casa de piedra, reformada para albergar a 18 residentes.
    La construcción y funcionamiento de la casa fue pensada con el concepto del respeto por el medio ambiente.
    Dispone de calefacción central a leña de alto rendimiento, sistema natural de recupero de aguas grises y materiales ecológicos térmicos.
    La casa está rodeada de pequeñas sierras, en un espacio sereno, seguro y discreto.
    Cuenta con un circuito aledaño de 2 km, donde los residentes pueden disfrutar de recorridas y realizar actividad física.</p>
